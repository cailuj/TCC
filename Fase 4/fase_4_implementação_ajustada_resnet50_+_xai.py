def increase_brightness(img_array):
    return np.clip(img_array * 1.2, 0.0, 1.0).astype(np.float32)

def load_and_preprocess(path, label):
    full_path = tf.strings.join([BASE_PATH, path])
    img = tf.io.read_file(full_path)
    img = tf.image.decode_jpeg(img, channels=CHANNELS)
    img = tf.image.resize(img, IMG_SIZE)
    img = tf.cast(img, tf.float32) / 255.0

    img = tf.numpy_function(increase_brightness, [img], tf.float32)
    img.set_shape([*IMG_SIZE, CHANNELS])

    return img, label

