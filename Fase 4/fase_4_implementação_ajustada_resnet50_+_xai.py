    img_float = tf.cast(img, tf.float32) / 255.0

    def apply_crop():
        full_mask_path = tf.strings.join([BASE_PATH, mask_path])
        mask = tf.io.read_file(full_mask_path)
        mask = tf.image.decode_image(mask, channels=1, expand_animations=False)
        mask_bin = tf.cast(mask > 0, tf.int32)

        rows = tf.reduce_any(mask_bin > 0, axis=[1, 2])
        cols = tf.reduce_any(mask_bin > 0, axis=[0, 2])

        row_indices = tf.where(rows)
        col_indices = tf.where(cols)

        def valid_bbox():
            rmin = tf.cast(row_indices[0][0], tf.int32)
            rmax = tf.cast(row_indices[-1][0], tf.int32)
            cmin = tf.cast(col_indices[0][0], tf.int32)
            cmax = tf.cast(col_indices[-1][0], tf.int32)

            pad = tf.constant(10, dtype=tf.int32)
            rmin = tf.maximum(0, rmin - pad)
            rmax = tf.minimum(tf.shape(img)[0], rmax + pad)
            cmin = tf.maximum(0, cmin - pad)
            cmax = tf.minimum(tf.shape(img)[1], cmax + pad)

            cropped_img = tf.image.crop_to_bounding_box(img_float, rmin, cmin, rmax - rmin, cmax - cmin)
            return tf.image.resize(cropped_img, IMG_SIZE)

        def invalid_bbox():
            return tf.image.resize(img_float, IMG_SIZE)

        return tf.cond(tf.logical_and(tf.size(row_indices) > 0, tf.size(col_indices) > 0), valid_bbox, invalid_bbox)

    def return_original():
        return tf.image.resize(img_float, IMG_SIZE)

    has_mask = tf.math.not_equal(mask_path, '-')
    final_img = tf.cond(has_mask, apply_crop, return_original)

    final_img.set_shape([*IMG_SIZE, CHANNELS])
    return final_img, label

